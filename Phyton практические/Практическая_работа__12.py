# Блок Б задание 4
# Дано натуральное число n>1. Проверьте, является ли оно простым. Программа должна вывести слово YES, если число простое и NO, если число составное. Алгоритм должен иметь сложность O(logn). Указание. Понятно, что задача сама по себе нерекурсивна, т.к. проверка числа n на простоту никак не сводится к проверке на простоту меньших чисел. Поэтому нужно сделать еще один параметр екурсии: делитель числа, и именно по этому параметру и делать рекурсию. 

def is_prime_recursive(n, divisor=None):
    if divisor is None:
        divisor = 2
    
    # Базовые случаи

    if n == 2:
        return "YES"
    if n % 2 == 0:
        return "NO"
    
    # Если делитель превысил √n, значит число простое

    if divisor * divisor > n:
        return "YES"
    
    # Если n делится на divisor, значит составное

    if n % divisor == 0:
        return "NO"
    
    return is_prime_recursive(n, divisor + 2)
